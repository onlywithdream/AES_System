static inline uint8_t xtime(const uint8_t b)
{
    return (b&0x80) ? ((b<<1)^0x1b) : (b<<1);
}

static inline void add_roundkey(uint32_t* s, const uint32_t* rkey)
{
    for (uint32_t* s_end = s + 4; s < s_end; s++, rkey++)
    {
        *s ^= *rkey;
    }
}

static inline void sub_bytes(uint32_t* s, const uint8_t inv)
{
    uint8_t* b = (uint8_t*)s;
    const uint8_t* sbox_start = inv ? sbox + 256 : sbox;
    for (uint8_t* b_end = b + 16; b < b_end; b++)
    {
        *b = *(sbox_start + *b);
    }
}

static void shift_rows(uint32_t* s, const uint8_t inv)
{
    int temp;
    uint8_t* b = (uint8_t*)s;
    if (inv)
    {
        temp = b[13];
        b[13] = b[9];
        b[9] = b[5];
        b[5] = b[1];
        b[1] = temp;
        temp = b[3];
        b[3] = b[7];
        b[7] = b[11];
        b[11] = b[15];
        b[15] = temp;
    }
    else
    {
        temp = b[1];
        b[1] = b[5];
        b[5] = b[9];
        b[9] = b[13];
        b[13] = temp;
        temp = b[15];
        b[15] = b[11];
        b[11] = b[7];
        b[7] = b[3];
        b[3] = temp;
    }
    temp = b[2];
    b[2] = b[10];
    b[10] = temp;
    temp = b[6];
    b[6] = b[14];
    b[14] = temp;
}

static void mix_col(uint32_t* s, const uint8_t inv)
{
    uint8_t b[4];
    *((uint32_t*)b) = *s;
    uint8_t temp[4] = {xtime(b[0]), xtime(b[1]), xtime(b[2]), xtime(b[3])};
    ((uint8_t*)s)[0] = temp[0]^b[1]^temp[1]^b[2]^b[3];
    ((uint8_t*)s)[1] = b[0]^temp[1]^b[2]^temp[2]^b[3];
    ((uint8_t*)s)[2] = b[0]^b[1]^temp[2]^b[3]^temp[3];
    ((uint8_t*)s)[3] = b[0]^temp[0]^b[1]^b[2]^temp[3];
    if (!inv) return;
    temp[0] = xtime(xtime(((uint8_t*)s)[0]))^xtime(xtime(((uint8_t*)s)[2]));
    temp[1] = xtime(xtime(((uint8_t*)s)[1]))^xtime(xtime(((uint8_t*)s)[3]));
    ((uint8_t*)s)[0] ^= temp[0];
    ((uint8_t*)s)[1] ^= temp[1];
    ((uint8_t*)s)[2] ^= temp[0];
    ((uint8_t*)s)[3] ^= temp[1];
}

static inline void mix_cols(uint32_t* s, const uint8_t inv)
{
    for (uint32_t* s_end = s + 4; s < s_end; s++)
    {
        mix_col(s, inv);
    }
}

static inline void rot_word(uint32_t* w)
{
    *w = (*w<<24)|(*w>>8);
}

static inline void sub_word(uint32_t* w)
{
    uint8_t* b = (uint8_t*)w;
    for (uint8_t* b_end = b + 4; b < b_end; b++)
    {
        *b = *(sbox + *b);
    }
}

void key_expansion(const uint32_t* key, uint32_t* rkey, const uint8_t nk)
{
    if (key != rkey)
    {
        for (uint32_t* rkey_end = rkey + nk; rkey < rkey_end; key++, rkey++)
        {
            *rkey = *key;
        }
    }
    for (int i = nk; i < 4*(nk+7); i++, rkey++)
    {
        *rkey = *(rkey-1);
        if (i % nk == 0)
        {
            rot_word(rkey);
            sub_word(rkey);
            ((uint8_t*)rkey)[0] ^= rcon[i/nk-1];
        }
        else if (nk > 6 && i % nk == 4)
        {
            sub_word(rkey);
        }
        *rkey ^= *(rkey-nk);
    }
}

void cipher(const uint32_t* ptext, uint32_t* ctext, const uint32_t* rkey, const uint8_t nk)
{
    if (ptext != ctext)
    {
        for (int i = 0; i < 4; i++)
        {
            ctext[i] = ptext[i];
        }
    }
    const uint32_t* rkey_end = rkey + (nk+6)*4;
    add_roundkey(ctext, rkey);
    for (rkey+=4; rkey < rkey_end; rkey+=4)
    {
        sub_bytes(ctext, 0);
        shift_rows(ctext, 0);
        mix_cols(ctext, 0);
        add_roundkey(ctext, rkey);
    }
    sub_bytes(ctext, 0);
    shift_rows(ctext, 0);
    add_roundkey(ctext, rkey);
}

void inv_cipher(const uint32_t* ctext, uint32_t* ptext, const uint32_t* rkey, const uint8_t nk)
{
    if (ctext != ptext)
    {
        for (int i = 0; i < 4; i++)
        {
            ptext[i] = ctext[i];
        }
    }
    const uint32_t* rkey_end = rkey + (nk + 6) * 4;
    add_roundkey(ptext, rkey_end);
    for (rkey_end-=4; rkey_end > rkey; rkey_end-=4)
    {
        shift_rows(ptext, 1);
        sub_bytes(ptext, 1);
        add_roundkey(ptext, rkey_end);
        mix_cols(ptext, 1);
    }
    shift_rows(ptext, 1);
    sub_bytes(ptext, 1);
    add_roundkey(ptext, rkey);
}